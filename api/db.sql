CREATE TABLE IF NOT EXISTS categories(
    id integer PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    name text NOT NULL CHECK (name <> ''),
    icon text
);

CREATE TABLE IF NOT EXISTS properties(
    id integer PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    name text NOT NULL CHECK (name <> ''),
    categoryProperty INTEGER REFERENCES categories(id)
);

CREATE TABLE IF NOT EXISTS vehicles(
    id integer PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    name text NOT NULL CHECK (name <> ''),
    patent text NOT NULL CHECK (name <> '')
);

CREATE TABLE IF NOT EXISTS propertyValues(
    id integer PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    value integer NOT NULL,
    valueVehicle integer REFERENCES vehicles(id),
    valueProperty integer REFERENCES properties(id)
);





